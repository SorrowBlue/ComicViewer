@Composable
fun ComicTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Dynamic color is available on Android 12+
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit,
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        darkTheme -> darkScheme
        else -> lightScheme
    }
    val windowSizeClass = currentWindowAdaptiveInfo().windowSizeClass
    val dimension by remember(windowSizeClass.windowWidthSizeClass) {
        mutableStateOf(
            when (windowSizeClass.windowWidthSizeClass) {
                WindowWidthSizeClass.COMPACT -> compactDimension
                WindowWidthSizeClass.MEDIUM -> mediumDimension
                WindowWidthSizeClass.EXPANDED -> expandedDimension
                else -> compactDimension
            }
        )
    }
    CompositionLocalProvider(LocalDimension provides dimension) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = AppTypography,
            content = content
        )
    }
}

val AppTypography = Typography()

object ComicTheme {

    val dimension: Dimension
        @Composable
        @ReadOnlyComposable
        get() {
            return LocalDimension.current
        }

    val colorScheme: ColorScheme
        @Composable
        @ReadOnlyComposable
        get() = MaterialTheme.colorScheme

    val typography: Typography
        @Composable
        @ReadOnlyComposable
        get() = MaterialTheme.typography

    val shapes: Shapes
        @Composable
        @ReadOnlyComposable
        get() = MaterialTheme.shapes

    var fixedSegmentedButtonColorsCached: SegmentedButtonColors? = null
}

@Composable
fun SegmentedButtonDefaults.fixedColors() = ComicTheme.fixedSegmentedButtonColorsCached ?: colors(
    disabledInactiveBorderColor = ComicTheme.colorScheme.onSurface.copy(alpha = 0.12f),
    disabledInactiveContentColor = ComicTheme.colorScheme.onSurface.copy(alpha = 0.38f)
).also {
    ComicTheme.fixedSegmentedButtonColorsCached = it
}
